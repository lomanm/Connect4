package gamesmodel;

public class C4Player {
	private String name;
    private String color;
    
	public C4Player(){		// constructor
	}

	public void setName(String name){
		this.name = name;
	}
	public String getName(){
		return name;
	}
	public void setColor(String color){
		this.color = color;
	}
	public String getColor(){
		return color;
	}
}
